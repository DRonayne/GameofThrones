    /**
     * Search for an actor image URL by name
     *
     * @param name The actor name to search for (case-insensitive)
     * @return The image URL if found, null otherwise
     */
    fun findActorImageUrl(name: String): String? {
        actorImages[name]?.let { return it }

        val normalizedName = name.trim()
        return actorImages.entries
            .firstOrNull { it.key.equals(normalizedName, ignoreCase = true) }
            ?.value
    }

    /**
     * Search for a character image URL by name
     *
     * @param name The character name to search for (case-insensitive)
     * @return The image URL if found, null otherwise
     */
    fun findCharacterImageUrl(name: String): String? {
        characterImages[name]?.let { return it }

        val normalizedName = name.trim()
        return characterImages.entries
            .firstOrNull { it.key.equals(normalizedName, ignoreCase = true) }
            ?.value
    }
